# frozen_string_literal: true

class Users::RegistrationsController < Devise::RegistrationsController
  before_action :ensure_tenant_present
  
  # POST /resource
  def create
    build_resource(sign_up_params)
    
    # Assign the current tenant to the user
    resource.tenant = ActsAsTenant.current_tenant
    
    resource.save
    yield resource if block_given?
    if resource.persisted?
      if resource.active_for_authentication?
        set_flash_message! :notice, :signed_up
        sign_up(resource_name, resource)
        respond_with resource, location: after_sign_up_path_for(resource)
      else
        set_flash_message! :notice, :"signed_up_but_#{resource.inactive_message}"
        expire_data_after_sign_up!
        respond_with resource, location: after_inactive_sign_up_path_for(resource)
      end
    else
      clean_up_passwords resource
      set_minimum_password_length
      respond_with resource
    end
  end

  protected
  
  def ensure_tenant_present
    unless ActsAsTenant.current_tenant
      redirect_to root_url(subdomain: nil), alert: 'Please access a specific workspace to register.'
    end
  end

